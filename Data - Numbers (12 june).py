from math import *  # Imports math functions like floor, ceil, sqrt, etc.

print(2)             # Prints an integer
print(2.53)          # Prints a float
print(-23.23)        # Prints a negative float

print(3 + 5)         # Addition => 8
print((2 + 5) * 34)  # Parentheses first, then multiply => 238
print(10 % 3)        # Modulus (remainder) => 1

my_num = -2

print(my_num)                          # Prints the number
print(str(my_num) + " number... yea!") # Concatenates after converting to string
print(abs(my_num))                     # Absolute value => 2
print(pow(my_num, 11))             # my_num to the power of 11 => -2048
print(max(4, 6))                       # Larger number => 6
print(min(4, 6))                       # Smaller number => 4
print(round(3.7))                      # Round to nearest integer => 4
print(floor(3.7))                      # Round down => 3
print(ceil(3.7))                       # Round up => 4
print(sqrt(36))                        # Square root => 6.0
